package GameModel;

/**
 * This enum is used to represent the different locations on a Players body that
 * he can equip items at.
 * 
 * @author Matthew Latura
 * 
 */
public enum EquipSlot {
	/**
	 * The item should be equipped onto the head.
	 */
	HEAD,
	/**
	 * The item should be equipped onto the torso.
	 */
	TORSO,
	/**
	 * The item should be equipped onto the legs.
	 */
	LEGS,
	/**
	 * The item should be equipped into the hands.
	 */
	HAND;
}
